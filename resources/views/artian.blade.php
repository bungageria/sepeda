    popularitas sepeda di Indonesia mulai didominasi oleh sepeda modern, seperti sepeda gunung (Mountain Bike), sepeda perkotaan (Commuting Bike), sepeda anak dan belakangan sepeda lipat (Folding Bike).</p>

