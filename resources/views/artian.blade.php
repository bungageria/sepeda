<p>Pada tahun 1839, Kirkpatrick MacMillan seorang pandai besi dari Skotlandia mengenalkan alat pendorong sepeda berupa engkol yang digunakan melalui gerakan turun-naik kaki serupa dengan mengayuh pedal sepeda pada saat ini.
